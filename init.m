%% init_iapf.m - IAPF 多机路径规划与编队控制初始化
clear; clc;

% =========================================================================
% 1. 基本物理参数
% =========================================================================
num_agents = 6; % 无人机总数 (1 Leader + 5 Followers，契合 Pan 论文的规模)
m = 1;          % 质点模型质量 (F = ma -> a = F)

% =========================================================================
% 2. 通信拓扑定义 (领航者与跟随者、跟随者之间的连接)
% 1号机默认为领航者 (Leader)，2~6号机为跟随者 (Followers)
% =========================================================================
% [快捷修改] 定义 Leader (1号) 连向哪些 Follower？
leader_connections = [2, 3, 4]; % 比如 Leader 牵着前面的 3 架飞机

% [快捷修改] 定义 Follower 之间的连接 (双向/无向)
% 每行代表一条边: [i, j] 表示 i <-> j
follower_edges = [ ...
    2, 3;
    3, 5;
    4, 6 ...
];

% === 自动生成邻接矩阵 A ===
A = zeros(num_agents, num_agents);
% 写入 Leader 的连接 (在势场中，Leader和Follower互相拉扯，通常是双向的)
for k = 1:length(leader_connections)
    j = leader_connections(k);
    A(1, j) = 1; 
    A(j, 1) = 1; 
end
% 写入 Follower 之间的连接
for k = 1:size(follower_edges, 1)
    i = follower_edges(k, 1);
    j = follower_edges(k, 2);
    A(i, j) = 1;
    A(j, i) = 1; % 无向图保持对称
end

% =========================================================================
% 3. 期望编队形态 (核心桥接：从坐标差转换为相对距离 d_des)
% =========================================================================
% 先定义理想的几何阵型偏移 (相对于 Leader 的位置 [dx; dy; dz])
% 这里设计一个简单的 V 字型或楔形阵型
delta = zeros(3, num_agents);
delta(:, 1) = [  0;   0;  0]; % 1: Leader (原点)
delta(:, 2) = [-10;  10;  0]; % 2: 左翼1
delta(:, 3) = [-10; -10;  0]; % 3: 右翼1
delta(:, 4) = [-20;  20;  0]; % 4: 左翼2
delta(:, 5) = [-20; -20;  0]; % 5: 右翼2
delta(:, 6) = [-30;   0;  0]; % 6: 尾部

% 自动计算两两之间的期望距离矩阵 D_des (N x N)
% D_des(i, j) 就是节点 i 和节点 j 之间应该保持的标量距离 d_des
D_des = zeros(num_agents, num_agents);
for i = 1:num_agents
    for j = 1:num_agents
        D_des(i, j) = norm(delta(:, i) - delta(:, j));
    end
end

% =========================================================================
% 4. 障碍物与目标点属性 (确保在同一直线上以触发局部极小值)
% =========================================================================
q_start  = [0; 0; 0];          % Leader 的出发点
q_target = [100; 100; 100];    % 全局目标点位置

% 障碍物设置 (放置在起点和终点的连线中央，测试避障和防死锁)
obs_center = [50; 50; 50];     % 障碍物中心坐标
obs_rho0   = 25;               % 障碍物的影响范围 / 警戒半径 (超过此距离斥力为0)

% =========================================================================
% 5. 初始状态与控制参数
% =========================================================================
% 初始位置：假设一开始大家都在起点附近，并且按照 delta 排好了松散的阵型
p0_all = repmat(q_start, 1, num_agents) + delta; 
v0_all = zeros(3, num_agents); % 初始速度全为 0

% ----------------- APF 控制系数 (参考 Pan 论文的设定) -----------------
k_t = 0.1;      % 目标引力增益 (k_t)
k_a = 100;     % 编队引斥力增益 (k_a)
k_r = 50;    % 障碍物斥力增益 (k_r) -> 对应 Ge & Cui 公式中的 eta
k_e = 50;     % 局部极小值旋转力增益 (k_e)
k_d = 5;      % 速度阻尼系数 (k_d，越大刹车越猛，防止震荡)

% Ge & Cui GNRON 改进特有参数
n_goal = 2;   % 目标距离因子的指数 (通常取 2)

% =========================================================================
% 6. 仿真运行参数
% =========================================================================
sim_time = 60;   % 仿真总时长(秒)
dt = 0.01;       % 采样步长

fprintf('IAPF 仿真参数初始化完成！\n');
fprintf('已生成 %d 个智能体网络，包含 1 个 Leader 与 %d 个 Followers。\n', num_agents, num_agents-1);