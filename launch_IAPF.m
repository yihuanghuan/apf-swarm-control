%% launch_IAPF.m - IAPF 多无人机编队仿真 一键启动脚本
% 功能：按顺序自动执行 初始化 -> 运行 Simulink -> 绘制结果

% 0. 清理环境 (极其重要，防止上一次的残余变量干扰本次仿真)
clear; clc; close all;
disp('==================================================');
disp('      🚀 开始执行 IAPF 多无人机仿真工作流');
disp('==================================================');

%% 1. 初始化阶段
disp('>>> [1/3] 正在加载物理参数与控制系数 (运行 init.m)...');
init;  % 直接调用你的 init.m 脚本
disp('    参数加载完成！');

%% 2. 仿真运行阶段
disp('>>> [2/3] 正在启动 Simulink 核心动力学引擎...');
disp('    仿真运行中，请耐心等待 (约几秒到几十秒)...');

% 核心指令：使用 sim() 函数在后台静默运行 Simulink 模型
% 注意：这里的 'IAPF' 必须和你的 Simulink 文件名严格一致 (不要加 .slx 后缀)
% sim() 函数会自动把 To Workspace 里的数据打包返回给 out 变量
try
    out = sim('IAPF'); 
    disp('    Simulink 仿真成功结束！');
catch ME
    % 捕获错误，防止 Simulink 报错导致整个程序崩溃而不知所措
    disp('    ❌ 仿真运行失败，请检查 Simulink 模型报错信息：');
    rethrow(ME);
end

%% 3. 数据后处理与绘图阶段
disp('>>> [3/3] 正在解析轨迹数据并生成可视化图表...');
plot_result; % 直接调用你的 plot_result.m 脚本
disp('    图表绘制完毕！');

disp('==================================================');
disp('      ✅ 全部任务完美执行！请查看弹出的 Figure 窗口');
disp('==================================================');